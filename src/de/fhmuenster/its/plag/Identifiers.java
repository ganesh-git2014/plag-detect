package de.fhmuenster.its.plag;

/**
 * Class to store the identifiers for the Greedy String Tiling algorithm
 * 
* @author arunjayapal
 * 
*/
public class Identifiers {

    /**
     * first identifier 
     */
    public final int id1;
    /**
     * second identifier
     */
    public final int id2;

    /**
     * public constructor with variable setter
     * @param id1
     * @param id2 
     */
    public Identifiers(int id1, int id2) {
        this.id1 = id1;
        this.id2 = id2;
    }
}
